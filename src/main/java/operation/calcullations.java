package operation;

public class calcullations {

    public static void main(String[] args) {
        int firstNumber = 9;
        int secondNumber = 3;
        double result = firstNumber / secondNumber;

        //% result is any excess
        System.out.println("Your excess is:"+firstNumber %secondNumber);

        //incrementation ++
        System.out.println(firstNumber++);
        System.out.println("You've added 1 and first number now is:" +firstNumber);

        // decrement
        System.out.println(firstNumber / secondNumber);
        System.out.println("Your first / second result is: "+ result);

    }
}
